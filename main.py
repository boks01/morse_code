def convert(text):
    char = list(text.lower())
    morse = {
        "a":"._",
        "b":"_...",
        "c":"_._.",
        "d":"_..",
        "e":".",
        "f":".._.",
        "g":"__.",
        "h":"....",
        "i":"..",
        "j":".___",
        "k":"_._",
        "l":"._..",
        "m":"__",
        "n":"_.",
        "o":"___",
        "p":".__.",
        "q":"__._",
        "r":"._.",
        "s":"...",
        "t":"_",
        "u":".._",
        "v":"._",
        "w":"..._",
        "x":"_.._",
        "y":"_.__",
        "z":"__..",
        ".":"._._._",
        ",":"__..__",
        ":":"___...",
        "-":"_..._",
        "/":"_.._.",
        "?":"..__..",
        "!":"_._.__",
        "&":"._...",
        "+":"._._.",
        "-":"_..._",
        "1":"_.._.",
        "2":"_.._.",
        "3":"_.._.",
        "4":"_.._.",
        "5":"_.._.",
        "6":"_.._.",
        "7":"_.._.",
        "8":"_.._.",
        "9":"_.._.",
        "0":"_.._.",
    }
    result = []
    for i in char:
        if i == " ":
            pass
        elif i in morse:
            result.append(morse[i])
    return " ".join(result)

